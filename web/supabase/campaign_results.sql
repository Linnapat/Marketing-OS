-- Campaign Result rows — per-ad performance tied to a campaign (campaign_id).
-- One row per ad/creative; the full editable object round-trips through `data`
-- (jsonb), matching the content_posts pattern. `row_id` is the stable id the UI
-- upserts on; `campaign_id` is mirrored for indexed per-campaign reads.

create table if not exists campaign_results (
  id           bigint generated by default as identity primary key,
  row_id       text unique not null,               -- res-CAM-2026-0001-01
  campaign_id  text references campaigns(id) on delete cascade,
  data         jsonb not null,                      -- whole CampaignResultRow
  created_at   timestamptz default now(),
  updated_at   timestamptz default now()
);

create index if not exists campaign_results_campaign_idx
  on campaign_results (campaign_id);

-- Keep updated_at fresh on every upsert.
create or replace function touch_campaign_results() returns trigger as $$
begin new.updated_at = now(); return new; end;
$$ language plpgsql;

drop trigger if exists trg_touch_campaign_results on campaign_results;
create trigger trg_touch_campaign_results
  before update on campaign_results
  for each row execute function touch_campaign_results();

-- RLS: same posture as the rest of the app — authenticated users read/write.
-- Tighten per your security_p1.sql conventions before go-live.
alter table campaign_results enable row level security;

drop policy if exists campaign_results_rw on campaign_results;
create policy campaign_results_rw on campaign_results
  for all to authenticated using (true) with check (true);
